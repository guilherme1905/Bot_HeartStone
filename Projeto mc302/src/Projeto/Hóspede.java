package Projeto;

public class Hóspede {

	protected String nome;
	protected String endereço;
	protected int documento;
	protected String nacionalidade;
	protected String email;
	protected int NumeroTelefone;
	
	public Hóspede (String nome1, String endereço1, int documento1,
			String nacionalidade1, String email1, int NumeroTelefone1){
		
		nome = nome1;
		endereço = endereço1;
		documento = documento1;
		nacionalidade = nacionalidade1;
		email = email1;
		NumeroTelefone = NumeroTelefone1;
		
	}
	
}
